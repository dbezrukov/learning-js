/*
The latest ECMAScript standard defines eight data types: 

Object

And 7 primitives data types:
- Boolean
- Null
- Undefined
- Number
- BigInt
- String
- Symbol

Objects are:
- "Normal" objects, and functions
- Dates
- Indexed collections: Arrays and typed Arrays
  Typed Arrays presents an array-like view of an underlying binary data buffer. 
- Keyed collections: Maps, Sets, WeakMaps, WeakSets
  Set and WeakSet represent a set of objects, while Map and WeakMap associate a value to an object. 
  The difference between Maps and WeakMaps is that in the former, object keys can be enumerated over. 
  This allows garbage collection optimizations in the latter case.
- Structured data: JSON

More info on objects: 
https://developer.mozilla.org/en-US/docs/Web/JavaScript/Reference/Global_Objects

*/
